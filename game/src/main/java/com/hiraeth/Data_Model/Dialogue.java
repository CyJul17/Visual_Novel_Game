package com.hiraeth.Data_Model;

public class Dialogue {

    public String name;
    public String text;
    public String background;
    public String image; 

    public Dialogue() {}

    public Dialogue(String name, String text, String background, String image) {
        
        this.name = name;
        this.text = text;
        this.background = background;
        this.image = image;
    }
}